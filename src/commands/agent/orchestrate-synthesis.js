/**
 * Artifact synthesis for orchestration Phase 0.
 * Combines explorer outputs into a unified codebase-map.md.
 */

import fs from 'fs';
import path from 'path';

/**
 * Synthesize codebase-map.md from explorer outputs.
 * @param {string} artifactDir - Directory containing structure.md, patterns.md, git-context.md
 * @returns {string} Combined markdown content
 */
export function synthesizeCodebaseMap(artifactDir) {
  const structurePath = path.join(artifactDir, 'structure.md');
  const patternsPath = path.join(artifactDir, 'patterns.md');
  const gitPath = path.join(artifactDir, 'git-context.md');

  // Read explorer outputs (gracefully handle missing files)
  const structure = readFileOrDefault(structurePath, 'No structure analysis available.');
  const patterns = readFileOrDefault(patternsPath, 'No patterns analysis available.');
  const gitContext = readFileOrDefault(gitPath, 'No git context available.');

  // Synthesize combined map
  const sections = [
    '# Codebase Map',
    '',
    'Generated by RUDI Orchestration Phase 0 explorers.',
    '',
    '---',
    '',
    '## Project Structure',
    '',
    structure.trim(),
    '',
    '---',
    '',
    '## Code Patterns & Conventions',
    '',
    patterns.trim(),
    '',
    '---',
    '',
    '## Git Context',
    '',
    gitContext.trim(),
    '',
    '---',
    '',
    '## Builder Notes',
    '',
    '- Follow established patterns from the "Code Patterns" section',
    '- Respect import conventions and file structure',
    '- Check "Git Context" for work-in-progress before modifying files',
    '- If this is a new project, establish conventions consistent with the tech stack',
  ];

  return sections.join('\n');
}

/**
 * Read file with fallback if it doesn't exist.
 */
function readFileOrDefault(filePath, defaultContent) {
  try {
    return fs.readFileSync(filePath, 'utf-8');
  } catch (err) {
    return defaultContent;
  }
}
